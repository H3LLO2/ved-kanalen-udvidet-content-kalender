// Establishment segment type
export type EstablishmentSegment = 1 | 2 | 3;

// Get strategy for specific phase (with optional segment for ESTABLISHMENT)
export function getPhaseStrategy(
  phase: keyof typeof PHASE_STRATEGIES,
  segment?: EstablishmentSegment
): string {
  if (phase === 'ESTABLISHMENT' && segment) {
    const segmentKey = `ESTABLISHMENT_SEGMENT_${segment}` as keyof typeof PHASE_STRATEGIES;
    return PHASE_STRATEGIES[segmentKey];
  }
  return PHASE_STRATEGIES[phase];
}
